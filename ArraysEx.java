class ArraysEx{
    public static void main(String args[]){
        int ar[]=new int[5];
        ar[0]=12;
        ar[1]=22;
        ar[2]=45;
        ar[3]=66;
        ar[4]=55;
        System.out.println("The Array elements are");
        for(int i=0;i<5;i++){
            System.out.println(ar[i]);
        }
    }
}